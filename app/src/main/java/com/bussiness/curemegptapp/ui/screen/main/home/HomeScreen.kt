@Composable
fun HomeScreen(
    navController: NavHostController,
    viewModel: HomeViewModel = hiltViewModel()
) {
    val uiState by viewModel.uiState.collectAsState()
    val selectedMood by viewModel.selectedMood
    val context = LocalContext.current
    val activity = context as? Activity
    var showMoodCard by remember {
        mutableStateOf(!SessionManager(context).isMoodGivenToday(context))
    }

    var showAlertDialog2 by remember { mutableStateOf(false) }
    var showAlertDialog3 by remember { mutableStateOf(false) }
    var showCompleteDialog by remember { mutableStateOf(false) }
    LaunchedEffect(Unit) {
        delay(5000) // â±ï¸ 5 seconds (4000 for 4 sec)
        showAlertDialog2 = true
    }
    if (showAlertDialog2) {
        AlertCardDialog(
            icon =  R.drawable.ic_medication,
            title = stringResource(R.string.medication_reminder_title)/*"Medication Reminder"*/,
            message =  stringResource(R.string.medication_reminder_message)/*"Time to take your"*/,
            highlightText = stringResource(R.string.medication_reminder_highlight)/*"Lisinopril 10mg."*/,
            warningText = stringResource(R.string.medication_reminder_warning)/*"Don't forget to take it with food"*/,
            cancelText = stringResource(R.string.snooze)/*"Snooze"*/,
            confirmText = stringResource(R.string.mark_as_taken)/*"Mark As Taken"*/,
            onDismiss = { showAlertDialog2 = false },
            onConfirm = { showAlertDialog2 = false
                showAlertDialog3 = true  }
        )

    }

    if (showAlertDialog3) {
        AlertCardDialog(
            icon =  R.drawable.ic_appointment,
            title = stringResource(R.string.appointment_reminder_title)/*"Dental Cleaning Today"*/,
            message = stringResource(R.string.appointment_reminder_message)/*"Don't forget your dental cleaning appointment "*/,
            highlightText = stringResource(R.string.appointment_reminder_highlight)/*"today at 2:00 PM with Dr. Sarah Johnson."*/,
            cancelText = stringResource(R.string.remind_later)/*"Remind Me Later"*/,
            confirmText = stringResource(R.string.got_it)/*"Got It"*/,
            onDismiss = { showAlertDialog3 = false },
            onConfirm = { showAlertDialog3 = false
                showCompleteDialog = true}
        )
    }
    if (showCompleteDialog) {
        CompleteProfileDialog(
            icon = R.drawable.ic_person_complete_icon,
            title = stringResource(R.string.complete_profile_title)/*"Complete Your Profile"*/,
            checklist = listOf(
                stringResource(R.string.complete_profile_benefit_1),
                stringResource(R.string.complete_profile_benefit_2),
                stringResource(R.string.complete_profile_benefit_3)
             /*   "Faster AI answers tailored to you",
                "Safer medication & allergy checks",
                "Quicker reminders & records Complete Now"*/
            ),
            cancelText = stringResource(R.string.remind_later)/*"Remind Me Later"*/,
            confirmText = stringResource(R.string.complete_now)/*"Complete Now"*/,
            skipText = stringResource(R.string.skip_for_now)/*"Skip for Now"*/,

            onDismiss = {
                showCompleteDialog = false
            },
            onConfirm = {
                showCompleteDialog = false
                navController.navigate(AppDestination.MainScreen)
            },
            onSkip = {
                showCompleteDialog = false
                // TODO â€“ "Skip for Now"
            }
        )
    }
    // ðŸ”¥ BACK PRESS â†’ EXIT APP
    BackHandler {
        activity?.finish()
    }
    // Initialize or refresh data when screen appears
    LaunchedEffect(Unit) {
        // Future mein: viewModel.refreshData()
        // For now, just use initial data
    }


    Column(
        modifier = Modifier
            .fillMaxSize()
            .background(color = Color.White)
            .statusBarsPadding()
            .padding(horizontal = 20.dp),
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        HomeHeader(
            logoRes = R.drawable.ic_logo,
            notificationRes = R.drawable.ic_notification_home_icon,
            profileRes = R.drawable.ic_profile_image,
            onClick = {
                navController.navigate(AppDestination.MyProfileScreen)
            },
            onClickNotification = {
                navController.navigate(AppDestination.AlertScreen)
            }
        )
        Column(
            modifier = Modifier
                .fillMaxSize()
                .verticalScroll(rememberScrollState()),
            verticalArrangement = Arrangement.SpaceEvenly,

            ) {
            Spacer(modifier = Modifier.height(23.dp))
            WelcomeSection(userGreating = uiState.userGreating, userName = uiState.userName)

            Spacer(modifier = Modifier.height(20.dp))
            if (showMoodCard) {
                DailyMoodCheckCard(
                    selectedMood = selectedMood,
                    onMoodSelected = { mood -> viewModel.updateMood(mood)
                        // âœ… Toast
                        Toast.makeText(
                            context,
                            "Thanks for giving your daily mood check ðŸ˜Š",
                            Toast.LENGTH_SHORT
                        ).show()

                        // âœ… Save date
                        SessionManager(context).saveMoodDate(context)

                        // âœ… Hide card
                        showMoodCard = false
                    },
                    // âŒ Close â†’ sirf hide
                    onClose = {
                        showMoodCard = false
                    },

                    // âŒ Skip â†’ sirf hide
                    onSkip = {
                        showMoodCard = false
                    }
                )
            }
            Spacer(modifier = Modifier.height(20.dp))
            ProfileCompletionBar(progress = uiState.profileCompletion)
            Spacer(modifier = Modifier.height(20.dp))
            Card(
                modifier = Modifier.fillMaxWidth(),
                colors = CardDefaults.cardColors(containerColor = Color(0xFFF9F9FD)),
                shape = RoundedCornerShape(30.dp)
            ) {
                MedicationsAndAllergies(
                    medications = uiState.medications,
                    allergies = uiState.allergies
                )

                Spacer(modifier = Modifier.height(20.dp))
                RecommendedSteps(steps = uiState.recommendedSteps)
            }

            Spacer(modifier = Modifier.height(20.dp))
            ThingsNeedingAttention(
                attentionItems = uiState.attentionItems,
                onScheduleClick = { itemId ->
                    viewModel.scheduleAttentionItem(itemId)
                },
                onViewAllClick = {
                    navController.navigate(AppDestination.ThingNeedingAttention)
                }

            )
            //
            Spacer(modifier = Modifier.height(20.dp))
            HealthOverviewSection(alerts = uiState.alerts, onAddClick = {
                navController.navigate("addFamilyMember?from=main")
            })
        }
    }
}











